    def get_burn_data(self, scope: str) -> Optional[BurnCalculationData]:
        """
        Get burn calculation data for a given scope.
        
        Args:
            scope: Scope identifier (e.g., "network", "campaign:123")
        
        Returns:
            BurnCalculationData if available, None otherwise
        """
        pass


